--Tao ds cac bang: Khoa chinh, Rang buoc: NULL/NOT NULL, DEFAULT, UNIQUE
CREATE TABLE KHOA
(
	MAKHOA varchar(5),
	TENKHOA nvarchar(30) NOT NULL UNIQUE,
	PHONG varchar(5),
	NAMTL int DEFAULT(1900),
	DIENTHOAI char(10),
	TRUONGKHOA varchar(5),
	NGAYNHAMCHUC date,
	CONSTRAINT PK_KHOA PRIMARY KEY (MAKHOA)
)

CREATE TABLE BOMON
(
	MABM nvarchar(5),
	TENBM nvarchar(30) NOT NULL UNIQUE,
	PHONG varchar(5),
	DIENTHOAI char(10),
	TRUONGBM varchar(5),
	MAKHOA varchar(5),
	NGAYNHAMCHUC date,
	CONSTRAINT PK_BOMON PRIMARY KEY (MABM)
)

CREATE TABLE GiAOVIEN
(
	MAGV varchar(5),
	HOTEN nvarchar(30),
	PHAI nvarchar(3) CHECK (PHAI in (N'Nữ','Nam')),
	NGSINH date,
	LUONG decimal(10,1) CHECK(LUONG>=0),
	DIACHI nvarchar(50),
	GVQLCM varchar(5),
	MABM nvarchar(5),
	CONSTRAINT PK_GIAOVIEN PRIMARY KEY (MAGV)
)

CREATE TABLE GV_DT
(
	MAGV varchar(5),
	DIENTHOAI char(10),
	CONSTRAINT PK_GV_DT PRIMARY KEY (MAGV,DIENTHOAI)
)

CREATE TABLE NGUOITHAN
(
	MAGV varchar(5),
	TEN nvarchar(30),
	NGSINH date,
	PHAI nvarchar(3) CHECK (PHAI in (N'Nữ','Nam')),
	CONSTRAINT PK_NGUOITHAN PRIMARY KEY (MAGV, TEN)
)

CREATE TABLE THAMGIADT
(
	MAGV varchar(5),
	MADT varchar(5),
	STT varchar(5),
	PHUCAP decimal(10,1),
	KETQUA nvarchar(10),
	CONSTRAINT PK_THAMGIADT PRIMARY KEY (MAGV,MADT,STT)
)

CREATE TABLE CONGVIEC
(
	MADT varchar(5),
	SOTT varchar(5),
	TENCV nvarchar(30),
	NGAYBD date,
	NGAYKT date,
	CONSTRAINT PK_CONGVIEC PRIMARY KEY (MADT,SOTT)
)
CREATE TABLE DETAI
(
	MADT varchar(5),
	TENDT nvarchar(50),
	CAPQL nvarchar(30),
	KINHPHI decimal(10,1),
	NGAYBD date,
	NGAYKT date,
	MACD varchar(5),
	GVCNDT varchar(5),
	CONSTRAINT PK_DETAI PRIMARY KEY(MADT)
)

CREATE TABLE CHUDE
(
	MACD varchar(5),
	TENCD nvarchar(30),
	CONSTRAINT PK_CHUDE PRIMARY KEY(MACD)
)


